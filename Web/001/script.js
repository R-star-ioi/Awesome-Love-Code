// 随机排列问题
let remainingQuestions = [...questions];

function getRandomQuestion() {
  const randomIndex = Math.floor(Math.random() * remainingQuestions.length);
  return remainingQuestions.splice(randomIndex, 1)[0];
}

let currentQuestion = null;

const questionEl = document.getElementById("question");
const optionsEl = document.getElementById("options");
const feedbackEl = document.getElementById("feedback");
const nextBtn = document.getElementById("next-btn");

function loadQuestion() {
  if (remainingQuestions.length === 0) {
    questionEl.textContent = "答题完成！";
    optionsEl.innerHTML = "";
    feedbackEl.textContent = "感谢您的参与！";
    nextBtn.style.display = "none";
    return;
  }

  currentQuestion = getRandomQuestion();
  questionEl.textContent = currentQuestion.question;
  optionsEl.innerHTML = "";

  currentQuestion.options.forEach((option, i) => {
    const li = document.createElement("li");
    const button = document.createElement("button");
    button.textContent = option;
    button.addEventListener("click", () => checkAnswer(i, currentQuestion.answer));
    li.appendChild(button);
    optionsEl.appendChild(li);
  });

  feedbackEl.textContent = "";
  nextBtn.style.display = "none";
}

function checkAnswer(selected, correct) {
  if (selected === correct) {
    feedbackEl.textContent = "回答正确！";
    feedbackEl.style.color = "green";
  } else {
    feedbackEl.textContent = `回答错误！正确答案是：${currentQuestion.options[correct]}`;
    feedbackEl.style.color = "red";
  }
  nextBtn.style.display = "inline-block";
}

nextBtn.addEventListener("click", loadQuestion);

// 初始化加载第一题
loadQuestion();
