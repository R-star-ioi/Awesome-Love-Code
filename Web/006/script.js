// 洗牌函数，使用Fisher-Yates算法
function shuffleQuestions() {
    for (let i = questions.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [questions[i], questions[j]] = [questions[j], questions[i]]; // 交换位置
    }
}

// 初始时打乱题目顺序
shuffleQuestions();

let currentQuestionIndex = 0;
let selectedAnswers = [];

function loadQuestion() {
    const question = questions[currentQuestionIndex];
    document.getElementById("question-text").textContent = question.question;

    const optionsContainer = document.getElementById("options-container");
    optionsContainer.innerHTML = "";
    selectedAnswers = [];

    question.options.forEach((option, index) => {
        const optionElem = document.createElement("div");
        optionElem.classList.add("option");

        const checkbox = document.createElement("input");
        checkbox.type = "checkbox";
        checkbox.id = `option-${index}`;
        checkbox.value = index;
        checkbox.onchange = () => handleCheckboxChange(index);

        const label = document.createElement("label");
        label.setAttribute("for", checkbox.id);
        label.textContent = option;

        optionElem.appendChild(checkbox);
        optionElem.appendChild(label);
        optionsContainer.appendChild(optionElem);
    });

    document.getElementById("submit-btn").disabled = false;
    document.getElementById("result").style.display = "none";
}

function handleCheckboxChange(index) {
    const checkbox = document.getElementById(`option-${index}`);
    if (checkbox.checked) {
        selectedAnswers.push(index);
    } else {
        const answerIndex = selectedAnswers.indexOf(index);
        if (answerIndex !== -1) {
            selectedAnswers.splice(answerIndex, 1);
        }
    }
}

function submitAnswer() {
    const question = questions[currentQuestionIndex];
    const correctAnswers = question.correctAnswers;
    
    const isCorrect = selectedAnswers.length === correctAnswers.length &&
                      selectedAnswers.every(answer => correctAnswers.includes(answer));

    const resultText = document.getElementById("result-text");
    resultText.textContent = isCorrect ? "恭喜你，答对了！" : `很遗憾，答错了。正确答案是：${question.correctAnswers.map(index => question.options[index]).join(", ")}`;

    document.getElementById("submit-btn").disabled = true;
    document.getElementById("result").style.display = "block";
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < questions.length) {
        loadQuestion();
    } else {
        alert("你已经完成所有的题目！");
        document.location.reload();
    }
}

// 初始化
loadQuestion();
